void solve()
{
    ll p, q, b;
    cin >> p >> q >> b;
    ll g = __gcd(p, q);
    q /= g;
    while (q > 1)
    {
        ll g = __gcd(q, b);
        if (g == 1)
        {
            break;
        }
        while (q % g == 0)
        {
            q /= g;
        }
    }
    if (q > 1)
        cout << "NO" << endl;
    else
        cout << "YES" << endl;
}
